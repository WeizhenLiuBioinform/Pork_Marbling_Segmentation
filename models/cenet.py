class SPPblock(nn.Module):
    def __init__(self, in_channels):
        super(SPPblock, self).__init__()
        self.pool1 = nn.MaxPool2d(kernel_size=[2, 2], stride=2)
        self.pool2 = nn.MaxPool2d(kernel_size=[3, 3], stride=3)
        self.pool3 = nn.MaxPool2d(kernel_size=[5, 5], stride=5)
        self.pool4 = nn.MaxPool2d(kernel_size=[6, 6], stride=6)

        self.conv = nn.Conv2d(in_channels=in_channels, out_channels=1, kernel_size=1, padding=0)

    def forward(self, x):
        self.in_channels, h, w = x.size(1), x.size(2), x.size(3)
        self.layer1 = F.interpolate(self.conv(self.pool1(x)), size=(h, w), mode='bilinear', align_corners=True)
        self.layer2 = F.interpolate(self.conv(self.pool2(x)), size=(h, w), mode='bilinear', align_corners=True)
        self.layer3 = F.interpolate(self.conv(self.pool3(x)), size=(h, w), mode='bilinear', align_corners=True)
        self.layer4 = F.interpolate(self.conv(self.pool4(x)), size=(h, w), mode='bilinear', align_corners=True)

        out = torch.cat([self.layer1, self.layer2, self.layer3, self.layer4, x], 1)

        return out


class DecoderBlock(nn.Module):
    def __init__(self, in_channels, n_filters):
        super(DecoderBlock, self).__init__()

        self.conv1 = nn.Conv2d(in_channels, in_channels // 4, 1)
        self.norm1 = nn.BatchNorm2d(in_channels // 4)
        self.relu1 = nonlinearity

        self.deconv2 = nn.ConvTranspose2d(in_channels // 4, in_channels // 4, 3, stride=2, padding=1, output_padding=1)
        self.norm2 = nn.BatchNorm2d(in_channels // 4)
        self.relu2 = nonlinearity

        self.conv3 = nn.Conv2d(in_channels // 4, n_filters, 1)
        self.norm3 = nn.BatchNorm2d(n_filters)
        self.relu3 = nonlinearity

    def forward(self, x):
        x = self.conv1(x)
        x = self.norm1(x)
        x = self.relu1(x)
        x = self.deconv2(x)
        x = self.norm2(x)
        x = self.relu2(x)
        x = self.conv3(x)
        x = self.norm3(x)
        x = self.relu3(x)
        return x


class CENet(nn.Module):
    def __init__(self, num_classes=2, in_channels=3, backbone='resnet34', pretrained=False, output_stride=16, freeze_bn=False, freeze_backbone=False, **_):
        super(CENet, self).__init__()
        resnet = ResNet(in_channels=in_channels, output_stride=output_stride, pretrained=pretrained, backbone=backbone)
        # expansion = self.backbone.expansion
        expansion = 1
        if backbone in ['resnet18', 'resnet34']:
            expansion = 1
        else:
            expansion = 4
        # low_level_channels = 64*expansion
        filters = [64*expansion, 128*expansion, 256*expansion, 512*expansion]
        # resnet = models.resnet34(pretrained=True)
        self.encoder0 = resnet.layer0
        self.encoder1 = resnet.layer1
        self.encoder2 = resnet.layer2
        self.encoder3 = resnet.layer3
        self.encoder4 = resnet.layer4

        self.ca3 = CAM_Module(filters[2])
        self.pa3 = PAM_Module(filters[2])
        self.ca4 = CAM_Module(filters[3])
        self.pa4 = PAM_Module(filters[3])

        self.dblock = DACblock(512*expansion)
        self.spp = SPPblock(512*expansion)

        self.decoder4 = DecoderBlock(512*expansion+4, filters[2]) # 512 + SPP的4
        self.decoder3 = DecoderBlock(filters[2], filters[1])
        self.decoder2 = DecoderBlock(filters[1], filters[0])
        self.decoder1 = DecoderBlock(filters[0], filters[0])

        self.finaldeconv1 = nn.ConvTranspose2d(filters[0], 32, 4, 2, 1)
        self.finalrelu1 = nn.ReLU(True)
        self.finalconv2 = nn.Conv2d(32, 32, 3, padding=1)
        self.finalrelu2 = nn.ReLU(True)
        self.finalconv3 = nn.Conv2d(32, num_classes, 3, padding=1)

    def forward(self, x):
        # Encoder
        x = self.encoder0(x)
        e1 = self.encoder1(x)
        e2 = self.encoder2(e1)
        e3 = self.encoder3(e2)
        # ca3 = self.ca3(e3)
        # pa3 = self.pa3(e3)
        # e3 = ca3 + pa3
        e4 = self.encoder4(e3)
        # ca4 = self.ca4(e4)
        # pa4 = self.pa4(e4)
        # e4 = ca4 + pa4
        # Center
        e4 = self.dblock(e4)
        e4 = self.spp(e4)

        # Decoder
        d4 = self.decoder4(e4) + e3
        d3 = self.decoder3(d4) + e2
        d2 = self.decoder2(d3) + e1
        d1 = self.decoder1(d2)

        out = self.finaldeconv1(d1)
        out = self.finalrelu1(out)
        out = self.finalconv2(out)
        out = self.finalrelu2(out)
        out = self.finalconv3(out)

        return out # 不用在输出求概率
        # return F.sigmoid(out)
    
    def get_backbone_params(self):
        return chain(
            self.encoder0.parameters(), self.encoder1.parameters(), self.encoder2.parameters(), self.encoder3.parameters(), self.encoder4.parameters()
        )

    def get_decoder_params(self):
        return chain(
            self.dblock.parameters(), self.spp.parameters(),
            self.decoder4.parameters(), self.decoder3.parameters(), self.decoder2.parameters(), self.decoder1.parameters(),
            self.finaldeconv1.parameters(), self.finalrelu2.parameters(), self.finalconv2.parameters(), self.finalrelu2.parameters(), self.finalconv3.parameters()
        )

    def freeze_bn(self):
        for module in self.modules():
            if isinstance(module, nn.BatchNorm2d): module.eval()
    

